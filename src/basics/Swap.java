package basics;

public class Swap {

	public static void main(String[] args) {
		
		int a =32;
		int b = 42;
		
		int temp;
		
		System.out.println(a);
		System.out.println(b);
		
		
		  temp=a;
		  a=b;
		  b=temp;
		 
		
		
		/*
		 * a=a-b; b=a+b; a=b-a;
		 */
		System.out.println(a);
		System.out.println(b);

	}

}
